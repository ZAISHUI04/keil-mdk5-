#include "bsp_can.h"
#include "main.h"


extern CAN_HandleTypeDef hcan1;
extern CAN_HandleTypeDef hcan2;

void can_filter_init(void)
{

    CAN_FilterTypeDef can_filter_st;
    can_filter_st.FilterActivation = ENABLE;         //启用顾虑器
    can_filter_st.FilterMode = CAN_FILTERMODE_IDMASK;   //使用标识符屏蔽位模式
    can_filter_st.FilterScale = CAN_FILTERSCALE_32BIT;  //32位滤波器
    can_filter_st.FilterIdHigh = 0x0000;
    can_filter_st.FilterIdLow = 0x0000;
    can_filter_st.FilterMaskIdHigh = 0x0000;
    can_filter_st.FilterMaskIdLow = 0x0000;          //这表明过滤器将接收任何CAN标识符的消息
    can_filter_st.FilterBank = 0;            //表示过滤器的编号
    can_filter_st.FilterFIFOAssignment = CAN_RX_FIFO0;         //匹配的帧放入FIFO0中
    HAL_CAN_ConfigFilter(&hcan1, &can_filter_st);         //配置CAN过滤器
    HAL_CAN_Start(&hcan1);                           //启用
    HAL_CAN_ActivateNotification(&hcan1, CAN_IT_RX_FIFO0_MSG_PENDING);      //激活CAN控制器接收FIFO0消息挂起中断通知


    can_filter_st.SlaveStartFilterBank = 14;
    can_filter_st.FilterBank = 14;
    HAL_CAN_ConfigFilter(&hcan2, &can_filter_st);
    HAL_CAN_Start(&hcan2);
    HAL_CAN_ActivateNotification(&hcan2, CAN_IT_RX_FIFO0_MSG_PENDING);

                              //激活CAN控制器接收FIFO0消息挂起中断通知

}
